// Stages a window of archived releases into `src/content/docs/<slug>/` subtrees,
// then runs a single Astro build that emits Latest (the current checkout) at the
// root and each archived version under `/<slug>/`. See site/README.md.

import { execFileSync } from "node:child_process";
import { promises as fs, existsSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { generateExamples } from "./copy-examples.js";

const siteDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const repoDir = path.resolve(siteDir, "..");
const docsDir = path.join(siteDir, "src/content/docs");
const schemaDir = path.join(siteDir, "src/assets/schema");
const worktreeRoot = path.join(repoDir, ".docs-version-builds");

const REPO = "zarf-dev/zarf";
// Minors kept in the switcher, from the current major and the one before it.
// Majors older than the previous one are dropped entirely.
const KEEP_CURRENT_MAJOR = 10;
const KEEP_PREVIOUS_MAJOR = 3;

const VERSION_SLUG = /^v\d+-\d+$/;

function git(args, opts = {}) {
  return execFileSync("git", args, { cwd: repoDir, stdio: "inherit", ...opts });
}

function npm(args, cwd) {
  execFileSync("npm", args, { cwd, stdio: "inherit", env: process.env });
}

// ---------------------------------------------------------------------------
// Version discovery
// ---------------------------------------------------------------------------

const parseSemver = (tag) => tag.replace(/^v/, "").split(".").map(Number);
const minorKey = (tag) => tag.replace(/\.\d+$/, "");
const slugOf = (minor) => minor.replace(/\./g, "-");

function cmpMinorDesc(a, b) {
  const [aMaj = 0, aMin = 0] = parseSemver(a);
  const [bMaj = 0, bMin = 0] = parseSemver(b);
  return bMaj - aMaj || bMin - aMin;
}

// Keeps the newest minors of the current major and the one before it; drops
// older majors. Expects `minorsDesc` sorted newest-first.
function limitByMajor(minorsDesc) {
  const majorsDesc = [...new Set(minorsDesc.map((m) => parseSemver(m)[0]))].sort((a, b) => b - a);
  const [currentMajor, previousMajor] = majorsDesc;
  const keptByMajor = new Map();
  return minorsDesc.filter((minor) => {
    const major = parseSemver(minor)[0];
    const cap =
      major === currentMajor ? KEEP_CURRENT_MAJOR : major === previousMajor ? KEEP_PREVIOUS_MAJOR : 0;
    const kept = keptByMajor.get(major) ?? 0;
    if (kept >= cap) return false;
    keptByMajor.set(major, kept + 1);
    return true;
  });
}

// `ref` is the full tag (e.g. v0.76.3, shown in the switcher); `slug` is the
// minor (e.g. 0-76), keeping a version's URL stable across patches.
function toVersion(tag) {
  return { ref: tag, slug: slugOf(minorKey(tag)) };
}

// Released minors, newest first, capped per major.
async function discoverVersions() {
  const headers = { Accept: "application/vnd.github+json" };
  if (process.env.GITHUB_TOKEN) headers.Authorization = `Bearer ${process.env.GITHUB_TOKEN}`;
  const res = await fetch(`https://api.github.com/repos/${REPO}/releases?per_page=100`, { headers });
  if (!res.ok) {
    throw new Error(`GitHub API returned ${res.status} ${res.statusText} for ${REPO} releases`);
  }
  const releases = await res.json();
  const tags = releases.filter((r) => !r.prerelease && !r.draft).map((r) => r.tag_name);

  // Keep only the newest patch per minor.
  const newestByMinor = new Map();
  for (const tag of tags) {
    if (!/^v?\d+\.\d+\.\d+$/.test(tag)) continue;
    const minor = minorKey(tag);
    const current = newestByMinor.get(minor);
    if (!current || parseSemver(tag)[2] > parseSemver(current)[2]) {
      newestByMinor.set(minor, tag);
    }
  }

  const minorsDesc = [...newestByMinor.keys()].sort(cmpMinorDesc);
  const archived = limitByMajor(minorsDesc).map((m) => toVersion(newestByMinor.get(m)));
  return { latest: minorsDesc[0], archived };
}

// ---------------------------------------------------------------------------
// Staging
// ---------------------------------------------------------------------------

// Remove subtrees and schemas left by a previous run. The `latest/` schemas and
// the current checkout's examples are regenerated by `prebuild` during the build.
async function cleanStaged() {
  for (const entry of await fs.readdir(docsDir, { withFileTypes: true })) {
    if (entry.isDirectory() && VERSION_SLUG.test(entry.name)) {
      await fs.rm(path.join(docsDir, entry.name), { recursive: true, force: true });
    }
  }
  await fs.rm(schemaDir, { recursive: true, force: true });
}

// Renderable per-API-version schemas a release ships, keyed by the slug its docs
// pages request. Newer releases split schemas under `src/pkg/schema/`; releases
// predating the split only had the flat v1alpha1 schema at the repo root.
function apiSchemaSources(root) {
  const pkg = path.join(root, "src/pkg/schema");
  const v1alpha1 = path.join(pkg, "zarf-v1alpha1-schema.json");
  const v1beta1 = path.join(pkg, "zarf-v1beta1-package-schema.json");
  const sources = {
    v1alpha1: existsSync(v1alpha1) ? v1alpha1 : path.join(root, "zarf.schema.json"),
  };
  if (existsSync(v1beta1)) sources.v1beta1 = v1beta1;
  return sources;
}

// Copy a tag's docs into `src/content/docs/<slug>/` and stage its generated
// artifacts (examples, schema). Content renders with the current toolchain.
async function stageVersion({ ref, slug }) {
  const worktree = path.join(worktreeRoot, slug);
  console.log(`\n=== Staging ${slug} from ${ref} ===`);
  // CI often uses a shallow clone without tag commits; fetch the tag's commit.
  try {
    git(["fetch", "--depth=1", "origin", "tag", ref, "--no-tags"]);
  } catch {
    console.warn(`git fetch of tag ${ref} failed; assuming it is already present`);
  }
  git(["worktree", "add", "--detach", worktree, ref]);
  try {
    const dst = path.join(docsDir, slug);
    await fs.rm(dst, { recursive: true, force: true });
    await fs.cp(path.join(worktree, "site/src/content/docs"), dst, { recursive: true });
    // Examples and schema are generated artifacts, absent from the checkout.
    await generateExamples({
      examplesDir: path.join(worktree, "examples"),
      dstDir: path.join(dst, "ref/Examples"),
      ref,
    });
    const versionSchemaDir = path.join(schemaDir, slug);
    await fs.mkdir(versionSchemaDir, { recursive: true });
    for (const [api, src] of Object.entries(apiSchemaSources(worktree))) {
      await fs.cp(src, path.join(versionSchemaDir, `${api}.json`));
    }
  } finally {
    git(["worktree", "remove", "--force", worktree]);
  }
}

async function main() {
  const { latest, archived } = await discoverVersions();
  console.log(`Latest (root, tracks current checkout): ${latest ?? "(unknown)"}`);
  console.log(`Archived versions: ${archived.map((v) => v.ref).join(", ") || "(none)"}`);

  // Written before the build so the switcher in every version shows the same options.
  await fs.writeFile(
    path.join(siteDir, "versions.json"),
    JSON.stringify({ versions: archived }, null, 2) + "\n",
  );

  await cleanStaged();
  if (archived.length > 0) {
    await fs.rm(worktreeRoot, { recursive: true, force: true });
    for (const version of archived) {
      await stageVersion(version);
    }
    await fs.rm(worktreeRoot, { recursive: true, force: true });
  }

  // Single build. `prebuild` stages Latest's schema and examples first.
  npm(["run", "build"], siteDir);

  console.log("\nVersioned documentation build complete.");
}

await main();
